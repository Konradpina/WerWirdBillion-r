function questionmusic(){
    document.getElementById("QIntro").play()     
            setTimeout(()=>{
                document.getElementById("Questionmusic").play()
            },4000)
}

function nextquestion(){
    document.getElementById("infomusic").pause()
    document.getElementById("infomusic").time=0

    document.getElementById("infob").hidden=true
    document.getElementById("infob").style.zIndex=-1
    loadquestion()
    document.getElementById("Questionb").hidden=false
    questionmusic()
}

function displayinfo(){
    if(qustionarry[currentq].info==""){
        nextquestion()
    }
    document.getElementById("infob").hidden=false
    document.getElementById("infob").style.zIndex=1
    document.getElementById("infomusic").play()

    document.getElementById("infohead").innerHTML=qustionarry[currentq-1].info.head
    document.getElementById("info").innerHTML=qustionarry[currentq-1].info.i
}