var currentq=2;
var prozess=false

function startquiz(){
    document.getElementById("Startb").hidden=true
    document.getElementById("stratp").style.zIndex=0
    document.getElementById("Questionb").hidden=false
    loadquestion()
}
function apress(number){
    if(prozess){
        return
    }
    prozess=true
    document.getElementById("Questionmusic").pause()
    document.getElementById("Questionmusic").currentTime=0
    document.getElementById("suspense").play()
    document.getElementsByClassName("answer")[number].style.backgroundColor="orange"
    setTimeout(() => {
       
        if( document.getElementsByClassName("aanswer")[number].innerText==qustionarry[currentq].aa[0]){
            currentq++
            
            var words = document.getElementsByClassName("cob");
            for (var i = 0; i < words.length; i++) {
                words[i].style.color = "green";
            }
            document.getElementById("correct").play()
            document.getElementsByClassName("answer")[number].classList.add("canswer")
            document.getElementsByClassName("answer")[number].style.backgroundColor=""
            setTimeout(()=>{
                document.getElementById("Questionb").hidden=true
                document.getElementsByClassName("answer")[number].classList.remove("canswer")

                displayinfo()
            },2000)
           
            
        }else {
            document.getElementById("wrrong").play()
            document.getElementsByClassName("answer")[number].style.backgroundColor="red"
            
            setTimeout(() => {
                var words = document.getElementsByClassName("cob");
                for (var i = 0; i < words.length; i++) {
                    words[i].style.color = "red";
                }                
            }, 4500);

            setTimeout(()=>{
                document.getElementById("Startb").hidden=false
                document.getElementById("stratp").style.zIndex=2
                document.getElementById("Questionb").hidden=true
                currentq=0
                document.getElementsByClassName("answer")[number].style.backgroundColor="black"

            },10000)
            
        }    
    }, 9000);
        
}
